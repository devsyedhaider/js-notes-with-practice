// Find Item and calculate Total

let item = prompt("Enter Your item : Burger , Pizza , fries , drink ");

switch (item) {
  case "burger":
    console.log("Your Total is 300");
    break;

  case "pizza":
    console.log("Your Total is 800");
    break;

  case "fries":
    console.log("Your Total is 150");
    break;

  case "drink":
    console.log("Your Total is 100");
    break;

  default:
    console.log("Nothing in this items");
}
