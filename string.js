// String and String Methods

let string = "My Name is Syed Ali Haider";
console.log(string);

// find String lenght

let name = "haider";
console.log(name.length);

// UpperCase

console.log(name.toUpperCase());

// Lower case

console.log(name.toLowerCase());

// Slice

console.log(name.slice(4));

// Replace

let rep = name.replace(name, "hassan naqvi");
console.log(rep);

// Concat

let new_name = "hassan";
console.log(name + " " + new_name);

// Trim

let color = "White ";
console.log(color.trim());

// Reverse a String

let str = "haider";
let rev = str.split("").reverse().join("");
console.log(rev);
