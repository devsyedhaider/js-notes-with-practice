// Primitive Data Types

// String
let name = "haider";
name = "hassan";

// Boolean
let isAdult = 0; //true
isAdult = 1; //false

// Null
let person = null;
console.log(person);

// Undefine
let user;
console.log(user);

// simbol
let id = Symbol("id");
console.log(id);

// Number
let num = 1;
let floatNum = 2.1;
let bigNum = 21.5e-10;
console.log(num, floatNum, bigNum);

// Bigint
let Bigint = 10n;
console.log(Bigint);

// Non Primative Data Type

// Object
let data = {
  name: "haider",
  age: 19,
  class: "1st Year",
};

console.log(data);

// Arrays
let numbers = [1, 2, 3, "haider", { name: "haider" }, 4, 5];
console.log(numbers);

